package BaekJoon_inputoutput;

import java.util.Scanner;

public class BaekJun_08 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int a;
		int b;
		a=sc.nextInt();
		b=sc.nextInt();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
	}

}
